# Set
name = {"haya", "ayesha", "areeba"}
print(type(name))  # <class 'set'>

# List
name = ["haya", "ayesha", "areeba"]
print(type(name))  # <class 'list'>

# Tuple
name = ("haya", "ayesha", "areeba")
print(type(name))  # <class 'tuple'>

# Empty dictionary
name = {}
print(type(name))  # <class 'dict'>
